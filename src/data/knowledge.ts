// 搜索答案
export function searchAnswer(query: string): KnowledgeItem | null {
  const lowerQuery = query.toLowerCase();

  // 先精确匹配问题
  const exactMatch = KNOWLEDGE_BASE.find(
    item => item.question.includes(query) || item.question.toLowerCase().includes(lowerQuery)
  );
  if (exactMatch) return exactMatch;

  // 关键词匹配
  for (const item of KNOWLEDGE_BASE) {
    for (const keyword of item.keywords) {
      if (lowerQuery.includes(keyword)) {
        return item;
      }
    }
  }

  return null;
}

// 获取分类问题
export function getQuestionsByCategory(category: string): KnowledgeItem[] {
  return KNOWLEDGE_BASE.filter(item => item.category === category);
}
